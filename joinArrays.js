function concat(array1, array2) {
  array2.forEach(function(value) {
    array1.push(value);
  })

  return array1;
  //return array1.concat(array2);
}

function merge(array1, array2){
  array2.forEach(function(value , index) {
    array1.splice(index, 0, value);
  })
  array1.sort();
  return array1;
}

